package com.example.project_nanlina.parking;

public class PMItem2 {
    int kickboard;
    int bicycle;
    int number;

    public PMItem2(int kickboard, int bicycle, int number) {
        this.kickboard = kickboard;
        this.bicycle = bicycle;
        this.number = number;
    }

    public int getKickboard() {
        return kickboard;
    }

    public void setKickboard(int kickboard) {
        this.kickboard = kickboard;
    }

    public int getBicycle() {
        return bicycle;
    }

    public void setBicycle(int bicycle) {
        this.bicycle = bicycle;
    }

    public int getNumber() {
        return number;
    }

    public void setNumber(int number) {
        this.number = number;
    }
}
